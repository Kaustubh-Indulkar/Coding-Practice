#include <iostream>
using namespace std;


int main()
{
    int num1,num2;
    cout<<"Enter the value of First Number: "; /*'<<'is called as Insertion Operator*/
    cin>>num1; /*'>>'is called as Extraction Operator*/
    
    cout<<"Enter the value of Second Number: "; /*'<<'is called as Insertion Operator*/
    cin>>num2; /*'>>'is called as Extraction Operator*/
    
    cout<<"The Sum of the Numbers is :"<<num1+num2;
    return 0;

}


